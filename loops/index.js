// Os 3 principais loops em Js => For, While e Do-While

// For
// Imprimir os números de 1 a 5
for (let i = 1; i <= 5; i++) {
  console.log(`Variável i é ${i}`);
}

// While
// Imprimir os números de 1 a 5
let contador = 1;

while (contador <= 5) {
  console.log("Contador" + " " + contador);
  contador++;
}

// Do-While
let index = 1;

do {
console.log(`Current index is ${index}`);
index++;
} while (index <= 5);
